char	*ft_strcat(char *s1, const char *s2)
{
	char	*p1;
	char	*p2;

	p1 = s1;
	p2 = (char *)s2;
	while (*p1 != '\0')
		++p1;
	while (*p2 != '\0')
	{
		*p1 = *p2;
		++p1;
		++p2;
	}
	*p1 = '\0';
	return (s1);
}
