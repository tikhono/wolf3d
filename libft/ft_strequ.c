#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	char	*p1;
	char	*p2;

	p1 = (char *)s1;
	p2 = (char *)s2;
	if (p1 == NULL || p2 == NULL)
		return (0);
	while (*p1++ == *p2++)
		if (*p1 == '\0' && *p2 == '\0')
			return (1);
	return (0);
}
